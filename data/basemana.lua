---Get base mana for class at level.
---@param className string The uppercase class name.
---@param level integer
---@return integer
function _A.GetBaseMana(className, level)
    if className == "WARRIOR"
    or className == "ROGUE"
    or className == "DEATHKNIGHT" then
        return 0;
    end

    if not basemana[className] then
        error("Invalid class name "..className);
    end

    return basemana[className][level];
end
